package com.lolay.citygrid.content.places.search;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

import org.apache.commons.lang.builder.EqualsBuilder;
import org.apache.commons.lang.builder.HashCodeBuilder;
import org.apache.commons.lang.builder.ToStringBuilder;

@XmlRootElement(name="region")
@XmlAccessorType(value=XmlAccessType.FIELD)
public class SearchRegion implements Serializable {
	private static final long serialVersionUID = 1L;

	@XmlAttribute(name="type",required=true)
	private String type = null;
	@XmlAttribute(name="name",required=true)
	private String name = null;
	@XmlElement(name="latitude",required=true)
	private Double latitude = null;
	@XmlElement(name="longitude",required=true)
	private Double longitude = null;
	@XmlElement(name="default_radius",required=true)
	private Float defaultRadius = null;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getLatitude() {
		return latitude;
	}
	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}
	public Double getLongitude() {
		return longitude;
	}
	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}
	public Float getDefaultRadius() {
		return defaultRadius;
	}
	public void setDefaultRadius(Float defaultRadius) {
		this.defaultRadius = defaultRadius;
	}

	@Override
	public int hashCode() {
		return HashCodeBuilder.reflectionHashCode(this);
	}
	@Override
	public boolean equals(Object obj) {
	   return EqualsBuilder.reflectionEquals(this, obj);
	}
	@Override
	public String toString() {
	   return ToStringBuilder.reflectionToString(this);
	}
}